export function getLocalizedStorageText(key: string, languageId: LanguageId): string {
  const translations = STORAGE_TRANSLATIONS[key];
  if (!translations) {
    console.warn(`Missing storage translation for key: ${key}`);
    return key;
  }
  return translations[languageId] || translations[LANGUAGE_IDS.ENGLISH] || key;
}

export function getLocalizedIvJudgeText(iv: number | null, languageId: LanguageId): string {
  if (iv === null) {
    return getLocalizedStorageText("iv.decent", languageId);
  }
  if (iv === 31) return getLocalizedStorageText("iv.best", languageId);
  if (iv === 30) return getLocalizedStorageText("iv.fantastic", languageId);
  if (iv >= 26) return getLocalizedStorageText("iv.veryGood", languageId);
  if (iv >= 16) return getLocalizedStorageText("iv.prettyGood", languageId);
  if (iv >= 1) return getLocalizedStorageText("iv.decent", languageId);
  return getLocalizedStorageText("iv.noGood", languageId);
}

export function getLocalizedStatAbbreviation(abbr: string, languageId: LanguageId): string {
  const keyMap: Record<string, string> = {
    "HP": "stat.hp",
    "ATK": "stat.attack",
    "DEF": "stat.defense",
    "SpA": "stat.spatk",
    "SpD": "stat.spdef",
    "SPD": "stat.speed",
  };
  const key = keyMap[abbr] || keyMap[abbr.toUpperCase()] || `stat.${abbr.toLowerCase()}`;
  return getLocalizedStorageText(key, languageId);
}

export function getLocalizedNatureDisplay(nature: Nature, languageId: LanguageId): string {
  const natureName = getLocalizedStorageText(`nature.${Nature[nature].toLowerCase()}`, languageId);
  const neutralNatures = [Nature.Hardy, Nature.Docile, Nature.Serious, Nature.Bashful, Nature.Quirky];
  
  if (neutralNatures.includes(nature)) {
    const neutralLabel = getLocalizedStorageText("nature.neutral", languageId);
    return `${natureName} (${neutralLabel})`;
  }

  const modifiers: Record<number, { inc: string; dec: string }> = {
    [Nature.Lonely]: { inc: "ATK", dec: "DEF" },
    [Nature.Brave]: { inc: "ATK", dec: "SPD" },
    [Nature.Adamant]: { inc: "ATK", dec: "SpA" },
    [Nature.Naughty]: { inc: "ATK", dec: "SpD" },
    [Nature.Bold]: { inc: "DEF", dec: "ATK" },
    [Nature.Relaxed]: { inc: "DEF", dec: "SPD" },
    [Nature.Impish]: { inc: "DEF", dec: "SpA" },
    [Nature.Lax]: { inc: "DEF", dec: "SpD" },
    [Nature.Timid]: { inc: "SPD", dec: "ATK" },
    [Nature.Hasty]: { inc: "SPD", dec: "DEF" },
    [Nature.Jolly]: { inc: "SPD", dec: "SpA" },
    [Nature.Naive]: { inc: "SPD", dec: "SpD" },
    [Nature.Modest]: { inc: "SpA", dec: "ATK" },
    [Nature.Mild]: { inc: "SpA", dec: "DEF" },
    [Nature.Quiet]: { inc: "SpA", dec: "SPD" },
    [Nature.Rash]: { inc: "SpA", dec: "SpD" },
    [Nature.Calm]: { inc: "SpD", dec: "ATK" },
    [Nature.Gentle]: { inc: "SpD", dec: "DEF" },
    [Nature.Sassy]: { inc: "SpD", dec: "SPD" },
    [Nature.Careful]: { inc: "SpD", dec: "SpA" },
  };

  const mod = modifiers[nature];
  if (mod) {
    const incStat = getLocalizedStatAbbreviation(mod.inc, languageId);
    const decStat = getLocalizedStatAbbreviation(mod.dec, languageId);
    return `${natureName} (+${incStat}, -${decStat})`;
  }

  return natureName;
}

export function getLocalizedGenderName(gender: PokemonGender, languageId: LanguageId): string {
  if (gender === PokemonGender.Male) {
    return getLocalizedStorageText("gender.male", languageId);
  }
  if (gender === PokemonGender.Female) {
    return getLocalizedStorageText("gender.female", languageId);
  }
  return getLocalizedStorageText("gender.unknown", languageId);
}
